; ============================================================
;  cat.asm  --  Echo вход на выход (через систему прерываний `trap`)
;
;  Логика:
;    - Главная программа просто крутит цикл и ждёт прерываний.
;    - По прерыванию обработчик читает символ из порта ввода
;      (адрес 2046) и пишет его в порт вывода (адрес 2047).
;    - Если входной символ — 0 ('\0' — наш конец-потока маркер), HLT.
;
;  Этот пример требует, чтобы программа сама прописала адрес
;  обработчика в вектор прерывания (адрес 0).
; ============================================================

.equ IO_IN   2046
.equ IO_OUT  2047
.equ NULL    0

.data
.org 0                  ; адрес 0 — вектор прерывания
        .word irq_handler

.text
.org 1
_start:
        EI                  ; разрешить прерывания
main_loop:
        JMP main_loop       ; крутимся, пока не придёт прерывание; HLT
                            ; произойдёт из обработчика, когда увидим '\0'

irq_handler:
        ; здесь IE автоматически выключается (логика "реализма"),
        ; но мы используем IN_HANDLER флаг — он не даёт вложенных прерываний
        LI   R1, IO_IN
        LW   R2, 0(R1)      ; R2 = только что прибывший символ
        LI   R3, NULL
        BEQ  R2, R3, finish ; '\0' -> завершение
        LI   R1, IO_OUT
        SW   R2, 0(R1)      ; вывод символа
        IRET                ; возврат на сохранённый PC
finish:
        HLT
