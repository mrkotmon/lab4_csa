; ============================================================
;  hello.asm  --  Печатает "Hello, World!"
;  Демонстрирует:
;    - секции .data / .text
;    - pstr (Pascal-string): первое слово — длина, далее — символы
;    - memory-mapped I/O: запись в IO_OUTPUT_ADDR (адрес 2047) выводит символ
; ============================================================

.equ IO_OUT 2047            ; адрес порта вывода (memory-mapped)

.data
.org 100                    ; данные начнём с адреса 100
msg:    .string "Hello, World!"

.text
.org 1                      ; программа начинается с адреса 1
_start:
        LI   R1, msg        ; R1 = адрес строки
        LW   R2, 0(R1)      ; R2 = длина (первое слово — длина для pstr)
        ADDI R3, R1, 1      ; R3 = указатель на первый символ
        LI   R4, 0          ; R4 = счётчик i
        LI   R5, IO_OUT     ; R5 = адрес порта вывода
print_loop:
        BGE  R4, R2, done   ; if i >= len -> выйти
        LW   R6, 0(R3)      ; R6 = текущий символ
        SW   R6, 0(R5)      ; вывод символа
        ADDI R3, R3, 1      ; ++ указатель
        ADDI R4, R4, 1      ; ++ счётчик
        JMP  print_loop
done:
        HLT
